        public static T AsJson<T>(this string jsonAsText) where T : class
        {
            return JsonUtility.FromJson<T>(jsonAsText);
        }

        public static async Task<T> AsJson<T>(this Task<string> jsonAsTextTask) where T : class
        {
            return (await jsonAsTextTask).AsJson<T>();
        }

        public static bool StartsWith(this string text, string pattern, params char[] ignoreCharacters)
        {
            var ignoreCharsSet = new HashSet<char>(ignoreCharacters);

            var len = text.Length;
            var patLen = pattern.Length;
            int idx = 0;

            for (int i = 0; i < len; i++)
            {
                char c = text[i];
                if (ignoreCharsSet.Contains(c))
                {
                    continue;
                }

                if (c != pattern[idx])
                {
                    return false;
                }

                if (++idx == patLen)
                {
                    return true;
                }
            }

            return false;
        }

        public static char RemoveDiacritics(this char c)
        {
            foreach (KeyValuePair<string, string> entry in foreign_characters)
            {
                if (entry.Key.IndexOf(c) != -1)
                {
                    return entry.Value[0];
                }
            }
            return c;
        }

        public static string RemoveDiacritics(this string s)
        {
            //StringBuilder sb = new StringBuilder ();
            string text = "";


            foreach (char c in s)
            {
                int len = text.Length;

                foreach (KeyValuePair<string, string> entry in foreign_characters)
                {
                    if (entry.Key.IndexOf(c) != -1)
                    {
                        text += entry.Value;
                        break;
                    }
                }

                if (len == text.Length)
                {
                    text += c;
                }
            }
            return text;
        }

        public static string RemoveWhitespaces(this string s)
        {
            string text = "";

            foreach (var c in s)
            {
                if (!char.IsWhiteSpace(c))
                {
                    text += c;
                }
            }

            return text;
        }

